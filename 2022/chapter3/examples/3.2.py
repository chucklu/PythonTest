# 除法
a = 100/3
print("100/3={}".format(a))

# 向下取整
a = 100//3
print("100//3={}".format(a))

a = 123+4.0
print("123+4.0={}".format(a))

a = 10.0-1+2j
print("10.0-1+2j={}".format(a))

x = 3.141592653
a = x
x **= 3
print("{}**3={}".format(a, x))

length = abs(-3+4j)
print("abs(-3+4j)={}".format(length))

f = 10.99
a = int(f)
print("int({})={}".format(f, a))

a = complex(10.99)
print(a)
